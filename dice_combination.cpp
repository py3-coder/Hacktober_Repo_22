#include<bits/stdc++.h>
using namespace std;
#define ll long long
#define lli long long int
#define vi vector<int>
#define vvi vector<vi>
#define vll vector<long long>
#define vb vector<bool>
#define pb push_back
#define pii pair<int,int>
#define all(x) x.begin(),x.end()

const int maxi=1000001;
int mod=1e9 + 7;
int dp[maxi],n;
int solve(int left){
    if(dp[left]!=0){
        return dp[left];
    }
    for(int i=1;i<=6;i++){
        if(left-i>=0){
            dp[left]+=solve(left-i);
            dp[left]%=mod;
        }
    }
    return dp[left];
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    cin>>n;
    memset(dp,0,sizeof(dp));
    dp[0]=1;
    cout<<solve(n)<<endl;

    return 0;
}
